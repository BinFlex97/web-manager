function WatchProduct(tenSP, nhanHieu, giaSP, size, model, strap, hinhAnhSP, moTaSP, imgDetailSP) {
    this.name = tenSP;
    this.brand = nhanHieu;
    this.price = giaSP;
    this.size = size;
    this.model = model;
    this.strap = strap;
    this.img = hinhAnhSP;
    this.desc = moTaSP;
    this.imgDetail = imgDetailSP;
}